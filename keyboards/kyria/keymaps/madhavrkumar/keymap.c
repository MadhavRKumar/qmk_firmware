static void render_status(void) {
    // QMK Logo and version information
    if(timer_elapsed32(matrix_timer) > MATRIX_FRAME_DURATION) {
        matrix_timer = timer_read32();
        char digit;
        oled_set_cursor(x_pos, y_pos);
        switch (get_highest_layer(layer_state)) {
        case _QWERTY:
            digit =(random()%2 > 0) ? '/' : '\\';
            break;
        case _LOWER:
            digit =(random()%2 > 0) ? '1' : '0';
            break;
        case _RAISE:
            digit =(random()%2 > 0) ? '>' : '<';
            break;
        case _ADJUST:
            digit =(random()%2 > 0) ? '(' : ')';
            break;
        default:
            digit = '?';
    }
        oled_write_char(digit, false);
        x_pos = (x_pos+1) % oled_max_chars();
        if(x_pos == 0) {
            y_pos += 1;
            if(y_pos == oled_max_lines()) {
                y_pos = 0;
                oled_clear();
            }
        }
    }
}

